package Gun12;

public class e_Math {

    public static void main(String[] args) {

        int a = -12;
        int b = 2;

        System.out.println("a nın mutlak degeri = " + Math.abs(a)); // abs mutlak değeri verir. -->12

        System.out.println("a ve b den büyük olan = " + Math.max(a,b)); // max degeri bulur. --> 2

        System.out.println("a ve b den küçük olan = " + Math.min(a,b)); // min degeri bulur. --> -12

        System.out.println("16 nın karekökü = " + Math.sqrt(16)); // 4

        System.out.println("2 nin 3. kuvveti = " + Math.pow(2,3)); // 8

        System.out.println("round of 3.1 = " + Math.round(3.1)); // 4  en yakın sayıya yuvarlıyor
        System.out.println("round of 3.5 = " + Math.round(3.5)); // 4

        System.out.println("ceil of 3.1 = " + Math.ceil(3.1)); // 4 en yakın üstteki tam sayı
        System.out.println("ceil of 3.5 = " + Math.ceil(3.5)); // 4

        System.out.println("floor of 3.1 = " + Math.floor(3.1)); // 3 en yakın alttaki tam sayı
        System.out.println("floor of 3.5 = " + Math.floor(3.5)); // 3









    }
}

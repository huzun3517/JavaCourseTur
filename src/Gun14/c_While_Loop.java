package Gun14;

public class c_While_Loop {

    public static void main(String[] args) {
        // Ekrana 5 kez Merhaba yazdırınız.

        int sayac = 0;

        while (sayac < 5) {
            System.out.println(sayac + ".kez Merhaba");
            sayac++; // sayac = sayac + 1;
        }



    }
}

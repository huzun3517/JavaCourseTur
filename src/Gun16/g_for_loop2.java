package Gun16;

public class g_for_loop2 {

    public static void main(String[] args) {

        //1 den 10 a kadar(dahil) sayıları ekrana yzdırınız.
        // 10 dan 0 a kadar(dahil) sayıları ekrana yazdırınız.

        for (int i =0; i<=10; i++){
            System.out.println("i = " + i);
        }

        System.out.println("*******************************");

        for (int i=10; i >=0; i--){
            System.out.println("i = " + i);
        }
    }
}

package Gun09;

public class f_ArithmeticOperators2 {

    public static void main(String[] args) {

        int a = 10;
        int b = 5;

        //Çarpma işlemi
        System.out.println("Çarpma işlemi");
        System.out.println("a*b = " + (a*b));

        //Bölme işlemi
        System.out.println("Bölme işlemi");
        System.out.println("a/b = " + (a/b));

        //Modül işlemi : Bölümünden kalan
        System.out.println("Modül işlemi");
        System.out.println("a%b = " + (a%b));
        // Modül bölmünden kalan demektir, bu işaretin yüzde ile ilgisi yok.


    }
}
